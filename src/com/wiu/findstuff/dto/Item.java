package com.wiu.findstuff.dto;

public class Item {
	
	private int Sub_Cat_Id;
	private int Item_Id;
	private String Item_Name;
	private String Item_Desc;
	private String Item_Img;
	private int Item_Price;
	private int Item_Qty;
	/**
	 * @return the sub_Cat_Id
	 */
	public int getSub_Cat_Id() {
		return Sub_Cat_Id;
	}
	/**
	 * @param sub_Cat_Id the sub_Cat_Id to set
	 */
	public void setSub_Cat_Id(int sub_Cat_Id) {
		Sub_Cat_Id = sub_Cat_Id;
	}
	/**
	 * @return the item_Id
	 */
	public int getItem_Id() {
		return Item_Id;
	}
	/**
	 * @param item_Id the item_Id to set
	 */
	public void setItem_Id(int item_Id) {
		Item_Id = item_Id;
	}
	/**
	 * @return the item_Name
	 */
	public String getItem_Name() {
		return Item_Name;
	}
	/**
	 * @param item_Name the item_Name to set
	 */
	public void setItem_Name(String item_Name) {
		Item_Name = item_Name;
	}
	/**
	 * @return the item_Desc
	 */
	public String getItem_Desc() {
		return Item_Desc;
	}
	/**
	 * @param item_Desc the item_Desc to set
	 */
	public void setItem_Desc(String item_Desc) {
		Item_Desc = item_Desc;
	}
	/**
	 * @return the item_Img
	 */
	public String getItem_Img() {
		return Item_Img;
	}
	/**
	 * @param item_Img the item_Img to set
	 */
	public void setItem_Img(String item_Img) {
		Item_Img = item_Img;
	}
	/**
	 * @return the item_Price
	 */
	public int getItem_Price() {
		return Item_Price;
	}
	/**
	 * @param item_Price the item_Price to set
	 */
	public void setItem_Price(int item_Price) {
		Item_Price = item_Price;
	}
	/**
	 * @return the item_Qty
	 */
	public int getItem_Qty() {
		return Item_Qty;
	}
	/**
	 * @param item_Qty the item_Qty to set
	 */
	public void setItem_Qty(int item_Qty) {
		Item_Qty = item_Qty;
	}
	
}
